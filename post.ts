// ─── Публикация одного поста (следующего по очереди) ─────────────────────────

async function postNext() {
  // Берём индекс из переменной окружения или случайный
  const index = process.env.POST_INDEX
    ? parseInt(process.env.POST_INDEX)
    : Math.floor(Math.random() * POSTS.length);

  const post = POSTS[index % POSTS.length];

  await bot.api.sendMessage(CHANNEL_ID, post.text, {
    parse_mode: "Markdown",
    link_preview_options: { is_disabled: true },
  });

  console.log(`✅ Posted to ${CHANNEL_ID}: post #${index}`);
}

